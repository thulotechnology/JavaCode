package com.company;

public class SwapValue {
    public static void main(String[] args) {
        int num1 = 40;
        int num2 = 60;

        // Do Something here to swap the variable
        int temp = 40;
        num1 = 60;
        num2 = temp;

        int num = 5;
        int sq = num*num;
        System.out.println("The square of num is "+sq);

        System.out.println("The value of num1 is "+num1);
        System.out.println("The value of num2 is "+num2);

    }
}
