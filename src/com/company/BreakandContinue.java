package com.company;

public class BreakandContinue {
    public static void main(String[] args) {
        // break: It will break entire loop or condition
        // continue: It will bypass the loop.
        for(int i=1; i<=100; i++){
            if(i==5){
                break;
            }
            System.out.println(i);
        }

    }
}

