package com.company;

public class Testing {
    public static void main(String[] args) {
        System.out.println("Absolute value of -5 is " + Math.abs(-5));
        System.out.println("Ceiling of 4.4 is " + Math.ceil(4.4));
        System.out.println("Floor of 4.4 is " + Math.floor(4.4));
        System.out.println("Round of 4.4 is " + Math.round(4.4));
        System.out.println("Max of 4 and 5 is " + Math.max(4, 5));
        System.out.println("Min of 4 and 5 is " + Math.min(4, 5));
        System.out.println("4 to the power of 2 is " + Math.pow(4, 2));
        System.out.println("Square root of 16 is " + Math.sqrt(16));
        System.out.println("Random number is " + Math.random());
    }
}
